<h4 class="panel-title"> <a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="admission_related_faqs.php#collapse4" aria-expanded="false" aria-controls="collapse1">How long does it take to receive an Offer Letter/reply from the University once the application is submitted?
</a> </h4>
</div>
<div id="collapse4" class="panel-collapse collapse" role="tabpanel" aria-labelledby="2">
<div class="panel-body">
<div class="panel-body">
<p class="mb5 mt5">Once we receive the application, within 3 working days you will get any of the following reply on your admission application:- </p>
<ul class="pl30">
<li><strong>Offer Letter -</strong> On fulfilling the programme eligibility requirements, you will be issued an <a data-toggle="modal" data-target="#offer-letter">Offer Letter</a>. </li>
