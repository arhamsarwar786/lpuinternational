<h4 class="panel-title"> <a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="admission_related_faqs.php#collapse1" aria-expanded="false" aria-controls="collapse1">What is the admission application process for international students at LPU?</a> </h4>
</div>
<div id="collapse1" class="panel-collapse collapse" role="tabpanel" aria-labelledby="1">
<div class="panel-body">
<div class="panel-body">
Admission process at LPU is very convenient and can be completed in simple steps. You can even submit your admission application while at home:-<br>
<strong>1st STEP: - Submit your application form </strong>
<ul class="pl30">
<li>Submit application On-line: - Submit an online application and upload required documents by visiting to the link: - <a href="https://admit.lpu.in/" target="_blank">admit.lpu.in</a></li>
<li>Submit application Off-line: - Send duly filled and signed <a href="downloads/international-application-form.pdf" target="_blank">admission application form</a> along with required documents to <a href="../cdn-cgi/l/email-protection#7910170d57181d14100a0a1016170a3915090c571a16571017"><span class="__cf_email__" data-cfemail="83eaedf7ade2e7eeeaf0f0eaecedf0c3eff3f6ade0ecadeaed">[email&#160;protected]</span></a></li>
</ul>
Click on following programme types to know the documents required:-
<table class="table table-bordered mt20">
<tr>
<td><a data-toggle="modal" data-target="#DocDiploma">Diploma (after O Level)</a></td>
<td><a data-toggle="modal" data-target="#DocUG">Bachelor Degree</a></td>
<td><a data-toggle="modal" data-target="#DocPG"> Master Degree</a></td>
</tr>
<tr>
<td><a data-toggle="modal" data-target="#DocUG-LE">Diploma (after O Level)Lateral Entry</a></td>
<td><a data-toggle="modal" data-target="#DocUG-LE"> Bachelor Degree (Lateral Entry)</a></td>
<td><a data-toggle="modal" data-target="#DocPhDFT">PhD (Full time)</a> /<a data-toggle="modal" data-target="#DocPhDPT"> PhD (Part time)</a></td>
</tr>
</table>
<strong>2nd STEP: - Download your Offer Letter</strong>
<p class="mb5 mt5">Once your application is received, it would be reviewed and verified in terms of minimum eligibility required in the applied program. You would be issued an <a data-toggle="modal" data-target="#offer-letter">Offer Letter</a> on fulfillment of eligibility criteria. In case of your results are awaited, you may be issued <a data-toggle="modal" data-target="#con-offer-letter">Conditional Offer Letter</a>. The letter can be downloaded by clicking on “Download Your Offer Letter” under this step. Unless and otherwise it would be informed through an email. </p>
<p><strong>Note:-</strong> Offer letter contains the information about applied programme, fee components, how to accept the offer of admission, fee structure document, how to pay fee, what to expect after making payment, important information terms & conditions, guidelines, reporting schedule, refund policy and important instructions for reporting at the University.</p>
<strong>3rd STEP: - Fee payment and updation</strong>
<p class="mb5 mt5">Once you receive your <a data-toggle="modal" data-target="#offer-letter">Offer Letter</a>/<a data-toggle="modal" data-target="#con-offer-letter">Conditional Offer Letter</a>, we suggest you to read it thoroughly in terms of all the information mentioned in it. You should transfer the “Minimum Provisional Registration Fee” to LPU’s bank account using “Fee Structure” document (3rd page of the Offer Letter) before the deadlines mentioned in the Offer Letter. The next step would be to upload following documents on the admission application portal or send through an email at <a href="../cdn-cgi/l/email-protection#dbb2b5aff5babfb6b2a8a8b2b4b5a89bb7abaef5b8b4f5b2b5"><span class="__cf_email__" data-cfemail="224b4c560c43464f4b51514b4d4c51624e52570c414d0c4b4c">[email&#160;protected]</span></a>: ‐</p>
<ul class="pl30 mb5">
<li>Proof of Payment made to LPU’s Bank account.</li>
<li>Duly filled and signed “Acceptance of Offer Letter” (4th page of the Offer Letter).</li>
<li>Copy of valid Passport (National Identity proof in case of Nepal and Bhutan)</li>
</ul>
<p>You should submit the above mentioned documents before the deadline as mentioned in the Offer Letter/Conditional Offer Letter to secure your seat in the applied programme.</p>
<strong>4th STEP: - Download your Acceptance Letter</strong>
<p class="mb5 mt5">Once the university receives and verifies the above mentioned documents, your provisional admission will be processed and an <a data-toggle="modal" data-target="#accept-letter">Acceptance Letter</a> will be issued that can be downloaded from the admission application portal. </p>
